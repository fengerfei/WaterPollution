package com.waterpollution.vo;

public class RankingVo {

	private String cityName;
	private int objectNums;
	private int compliantNums;

	public String getCityName() {
		return cityName;
	}

	public void setCityName(String cityName) {
		this.cityName = cityName;
	}

	public int getObjectNums() {
		return objectNums;
	}

	public void setObjectNums(int objectNums) {
		this.objectNums = objectNums;
	}

	public int getCompliantNums() {
		return compliantNums;
	}

	public void setCompliantNums(int compliantNums) {
		this.compliantNums = compliantNums;
	}

}
